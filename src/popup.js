function loadMHTML() {
  const mhtmlString = localStorage.getItem('saveMhtml');
  if (mhtmlString) {
    const mhtmlData = mhtmlString;

    const blob = new Blob([mhtmlData], {type: 'multipart/related'});
    const url = URL.createObjectURL(blob);

    chrome.tabs.update({url: url});
  }
}