let globalTabUrl = ""; 
let globalMhtmlString = ""; 

document.getElementById('saveHtml').addEventListener('click', () => {
  chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
    const currentTab = tabs[0]; 
    globalTabUrl = currentTab.url;

    chrome.pageCapture.saveAsMHTML({ tabId: currentTab.id }, (mhtmlData) => {
      const reader = new FileReader();
      reader.onload = async function(event) { 
        globalMhtmlString = event.target.result;

        localStorage.setItem('savedMhtml',globalMhtmlString);

        var compressed = pako.deflate(globalMhtmlString, { to: 'string' });
        var encodedString = btoa(compressed);
        globalMhtmlString = encodedString;
        await mintPost();
      };
      reader.readAsText(mhtmlData);
    });
  });
});

